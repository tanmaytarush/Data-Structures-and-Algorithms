// Let's deep dive into variables and it's different types

#include<iostream>
using namespace std;

int main()
{
    // Integer Point
    int a = 1;
    cout<<a<<endl;

    // Floating Point
    float b = 2.5;
    cout<<b<<endl;

    // Long and Double
    long c = 234;
    cout<<c<<endl;

    double d = 2345667;
    cout<<d<<endl;

    return 0;
}